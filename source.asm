PrintString MACRO String
    PUSHA
    MOV BX, offset String
    
    PrintAgain:
        MOV AH, 2
        MOV DL, [BX]
        INT 21H
        INC BX
        CMP [BX], '$'
        JNZ PrintAgain
    
    POPA
                
ENDM PrintString   





